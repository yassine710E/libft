#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		size_s;
	char	*ptr;

	i = 0;
	size_s = ft_strlen(s);
	ptr = malloc((size_s + 1) * sizeof(char));
	if (!ptr)
		return (NULL);
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
